const Post = require('../models/Post');
const path = require('path');
const fs = require('fs');

// READ: Get all posts for the logged-in user (Dashboard)
exports.getDashboard = async (req, res) => {
    try {
        // Find posts by this user and populate the 'author' 
        const posts = await Post.find({ author: req.session.user.id })
                                .populate('author', 'username')
                                .sort({ createdAt: -1 }); // Newest first
        res.render('dashboard', { posts, error: null });
    } catch (error) {
        console.error(error);
        res.status(500).send('Server Error');
    }
};

// CREATE: Render the create post form
exports.getCreatePost = (req, res) => {
    res.render('create-post', { error: null });
};

// CREATE: Handle the form submission and image upload
exports.postCreatePost = async (req, res) => {
    try {
        const { title, content } = req.body;
        let imagePath = '';

        // Validate and handle image upload
        if (req.files && req.files.image) {
            const image = req.files.image;
            
            // Check if the uploaded file is actually an image
            if (!image.mimetype.startsWith('image/')) {
                return res.render('create-post', { error: 'Invalid file type. Please upload an image.' });
            }

            // Create a unique filename and set the upload path
            const imageName = Date.now() + '-' + image.name.replace(/\s+/g, '-');
            const uploadPath = path.join(__dirname, '../public/uploads', imageName);
            
            // Move the file to our public/uploads directory
            await image.mv(uploadPath);
            imagePath = '/uploads/' + imageName;
        }

        // Save post to database
        const newPost = new Post({
            title,
            content, // This will be the HTML generated by Quill.js
            image: imagePath,
            author: req.session.user.id
        });

        await newPost.save();
        res.redirect('/dashboard');

    } catch (error) {
        console.error(error);
        res.render('create-post', { error: 'Something went wrong creating the post.' });
    }
};

// DELETE: Handle post deletion
exports.deletePost = async (req, res) => {
    try {
        const post = await Post.findOne({ _id: req.params.id, author: req.session.user.id });
        
        if (!post) {
            return res.status(404).send('Post not found or unauthorized');
        }

        // If the post has an image, delete it from the server
        if (post.image) {
            const imagePath = path.join(__dirname, '../public', post.image);
            if (fs.existsSync(imagePath)) {
                fs.unlinkSync(imagePath);
            }
        }

        await Post.findByIdAndDelete(req.params.id);
        res.redirect('/dashboard');
    } catch (error) {
        console.error(error);
        res.status(500).send('Server Error');
    }
};

