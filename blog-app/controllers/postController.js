// UPDATE: Handle the form submission and optional new image
exports.postEditPost = async (req, res) => {
    try {
        const { title, content } = req.body;
        const post = await Post.findOne({ _id: req.params.id, author: req.session.user.id });

        if (!post) {
            return res.status(404).send('Post not found or unauthorized');
        }

        // Update basic text fields
        post.title = title;
        post.content = content;

        // Handle new image upload if provided
        if (req.files && req.files.image) {
            const image = req.files.image;
            
            if (!image.mimetype.startsWith('image/')) {
                return res.render('edit-post', { post, error: 'Invalid file type. Please upload an image.' });
            }

            // Delete the old image from the server to save space
            if (post.image) {
                const oldImagePath = path.join(__dirname, '../public', post.image);
                if (fs.existsSync(oldImagePath)) {
                    fs.unlinkSync(oldImagePath);
                }
            }

            // Save the new image
            const imageName = Date.now() + '-' + image.name.replace(/\s+/g, '-');
            const uploadPath = path.join(__dirname, '../public/uploads', imageName);
            
            await image.mv(uploadPath);
            post.image = '/uploads/' + imageName;
        }

        // Save the updated post to MongoDB
        await post.save();
        res.redirect('/dashboard');

    } catch (error) {
        console.error(error);
        res.status(500).send('Server Error');
    }
};

